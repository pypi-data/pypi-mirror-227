def audio_log(_msg, elapsed_time = 0):
    global total_time

    if elapsed_time == 0:
        print(_msg)
        total_time = 0
    else:
        delta = millis() - elapsed_time
        total_time = total_time + delta
        print(_msg, round(delta, 2), "total", round(total_time, 2))

    return millis()

